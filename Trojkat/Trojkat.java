class Trojkat{
	public Trojkat(int a, int h){
		System.out.println("Podstawa: "+a+"\nWysokosc: "+h+"\nPole trojkata: "+a*h/2);
	}
	public Trojkat(int a, int b, int c, int r){
		System.out.println("\nDlugosc boku: "+a+"\nDlugosc boku: "+b+"\nDlugosc boku: "+c+"\nPromien okregu: "+r+"\nPole trojkata: "+a*b*c/4*r);
	}
}